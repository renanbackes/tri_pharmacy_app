package com.example.myapplication.model;

public class Gol {

    private Integer idGol;
    private String tpMinutoGol;
    private Integer idJogador;
    private Integer idPartida;
    private Integer idTime;

    public Integer getIdGol() {
        return idGol;
    }

    public void setIdGol( Integer idGol ) {
        this.idGol = idGol;
    }

    public String getTpMinutoGol() {
        return tpMinutoGol;
    }

    public void setTpMinutoGol( String tpMinutoGol ) {
        this.tpMinutoGol = tpMinutoGol;
    }

    public Integer getIdJogador() {
        return idJogador;
    }

    public void setIdJogador( Integer idJogador ) {
        this.idJogador = idJogador;
    }

    public Integer getIdPartida() {
        return idPartida;
    }

    public void setIdPartida( Integer idPartida ) {
        this.idPartida = idPartida;
    }

    public Integer getIdTime() {
        return idTime;
    }

    public void setIdTime( Integer idTime ) {
        this.idTime = idTime;
    }
}
